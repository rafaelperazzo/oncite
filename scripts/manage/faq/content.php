<h3>How do I add a member of staff in to the system?</h3>
<p>If you know the person's UCL user ID (a 7-character code), you can enter them
  yourself from the main page (look under the &quot;User management&quot; subheading).</p>
<p>If not, contact Dan Stowell, who will be able to look up names and add them
  to the database.</p>
<h3>Can academic staff input their own publications?</h3>
<p>Yes!</p>
<p>They need to know their main UCL user ID and password. They can then visit
  exactly the same page as publications administrators visit, but of course will
  not have quite such wide-ranging powers to approve/delete publications. (They
  also need to have been listed in the &quot;people&quot; database which this system uses
  - but if they're not, they'll get a friendly message telling them how to get
  registered.)</p>
<p>Remember that <strong><em>entries added by academic staff are held for confirmation
      by their department's publications administrator</em></strong> before they
      are made public.</p>
<h3>How do I approve publications which staff have added?</h3>
<p>Simply go to the main &quot;management&quot; page for the system - if there are any
  waiting to be approved then <strong>a large link named &quot;Approve publications&quot;
  will appear!</strong> Publications administrators will also occasionally
  receive an email providing a direct link, if entries are waiting.</p>
<h3>Can we add entries to past years?</h3>
<p>Yes - please do!</p>
<p>(Additions to past years of this database will not be passed across to the
  UCL Reference Manager / WebPoster database.)</p>
<h3>How far back in time should we go?</h3>
<p>UCL's administrative process for research publications goes back to 1997.
   Records prior to 1997 aren't complete, and there's no real need to add publications
  from before then. However, there is no harm in adding entries for publications
  as far back as you want to go.</p>
<h3>What happens with &quot;In press&quot; publications?</h3>
<p>&quot;In press&quot; publications are stored in the database quite happily, but aren't
  ordinarily displayed on the main &quot;Search&quot; pages which the public can use to
  browse our publications. They can be displayed on (for example) an academic's
  personal listing, which some departments make use of.</p>
<h3>Should we be associating publications with staff who have left?</h3>
<p>You don't need to, but it's fine if you do. When you enter a publication,
  the system automatically suggests people to associate it with, by looking up
  names in its database of people. If staff appear who are no longer working
  for UCL / your department, but who were involved in authoring the publication,
  please do tick the box next to their name - it's useful for historical reasons,
  but not necessary.</p>
<h3>Which department(s) should a publication be associated with?</h3>
<p>Only the ones which were involved in the production of the publication - <em><strong>not</strong></em> departments
  whose area the publication happens to cover, and <em><strong>not</strong></em> departments
  which one of the authors later (or earlier) joined, but which weren't actually
  involved at the time of the research/publication.</p>
<h3>The &quot;Check for duplicates&quot; function didn't find all the duplicates!</h3>
<p>Unfortunately it's quite difficult to create an &quot;intelligent&quot; duplicate check.
  The current version simply checks for publications <strong>from your own department</strong>, <strong>from
  the same year</strong>, whose
  <strong>titles are similar </strong>(this is calculated by an algorithm called
  &quot;Levenshtein&quot;) and whose <strong>start-pages
  are similar</strong>. If there's a duplication that falls outside of these
  boundaries then it doesn't get picked up, and you will need to solve it manually.</p>
